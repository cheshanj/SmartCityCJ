    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        GetSetForMaintenance mt = new GetSetForMaintenance();
        mt.setMidForRepair(lbl);
        mt.setFilterType(cmb_RSubType.getSelectedItem().toString());
        mt.setOtherChanges(txt_changes.getText());
        mt.setEmployee(list_emp.getSelectedValue().toString());
        mt.setRepairDate(txt_RepairDate.getText());
        mt.setRepairResult(txt_result.getText());
        
        
        MaintenanceDAO mc = new MaintenanceDAO();
        mc.SubmitOfRepair(mt);
    }//GEN-LAST:event_btn_submitActionPerformed

