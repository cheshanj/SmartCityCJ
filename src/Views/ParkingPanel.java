    private void loadStations() throws ClassNotFoundException, IOException  {
        try {
            allStations.addAll(Serialization.deserializeParkingStations());
            this.fillComboBox();
        } catch (IOException e) {
            Serialization.Serializing(allStations, FileName.STATION_PARKING);
        }
    }
    
    private void fillComboBox() {
        cmbStations.removeAllItems();
        for (SensorStation station : allStations) {
            cmbStations.addItem(station);
        }
    }
    
    private void loadSensorMonitorsTable() {
        if (this.selectedStation == null) {
            return;
        }
        sensorMonitors.removeAllElements();
        for (SensorMonitor monitor: selectedStation.getSensorsMonitors()) {
            sensorMonitors.add(monitor);
        }
        
        data = new Vector<Vector<String>>();
        header = new Vector<String>();

        header.add("Slot No");
        header.add("Slot ID");
        header.add("Slot Type");
        header.add("Price");

        for (SensorMonitor monitor : sensorMonitors) {
            ParkingSlot slot = (ParkingSlot) monitor;
            Vector v = new Vector();
            v.add(slot.getSlot_name());
            v.add(slot.getSensorMonitorID());
            v.add(slot.getSuitable());
            v.add(slot.getPrice());
            data.add(v);
        }
        
        tblSensors.setModel(new DefaultTableModel(data, header));
        TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tblSensors.getModel());
        tblSensors.setRowSorter(sorter);
        scrollSensors.setViewportView(tblSensors);
        tblSensors.setAutoCreateRowSorter(true);
        tblSensors.setAutoscrolls(true);
        tblSensors.getTableHeader().setReorderingAllowed(false);
    }
    
