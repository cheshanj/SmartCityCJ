    private void btnAddSlotSensorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSlotSensorActionPerformed
        
        if (this.selectedStation == null) {
            System.out.println("No selected station");
            return;
        }
        System.out.println("selectedStation monitors: "+ selectedStation.getSensorsMonitors());
        
        String slot_name = this.txtSlotNo.getText();
        String sensor_ID = this.txtSlotId.getText();
        String suitable = this.cmbSuitable.getSelectedItem().toString();
        String price = this.txtPrice.getText();
        
        ArrayList<Double> coords = new ArrayList<Double>();
        coords.add(69.786732);
        coords.add(81.340522);
        
        ParkingSlot slot = new ParkingSlot(sensor_ID, coords, true, SensorType.FLOOD);
        slot.setSlot_name(slot_name);
        slot.setSuitable(suitable);
        slot.setPrice(price);
        this.selectedStation.addNewSensorMonitor(slot);
        
        try {
            Serialization.Serializing(allStations, FileName.STATION_PARKING);
        } catch (IOException ex) {
            Logger.getLogger(ParkingPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        loadSensorMonitorsTable();
        
//        Clock.getInstance().registerObserver(slot);
    }//GEN-LAST:event_btnAddSlotSensorActionPerformed

    
    
